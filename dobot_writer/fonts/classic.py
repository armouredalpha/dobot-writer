class ClassicFont(Font):
    """
    Classic single-stroke font.

    Clean, minimal strokes — the fastest font to draw.  All letters are
    uppercase (lowercase input is silently mapped to uppercase).
    """

    name        = "classic"
    description = "Angular single-stroke letterforms — fast and clear"

    def get_glyph(self, char: str) -> Optional[Glyph]:
        key = char.upper() if len(char) == 1 else char
        return _CLASSIC_GLYPHS.get(key)  # None if not found

    def supported_chars(self) -> frozenset:
        return frozenset(_CLASSIC_GLYPHS.keys())
