"""
Rounded font — smooth, curvy single-stroke letterforms.

Circular and semi-circular letters (O, C, D, B, P, R, Q, U, J, S …) use
mathematically computed arc points for genuine curve quality.  Angular letters
use extra interpolated waypoints to soften corners.

Arc helper
──────────
  _a(cx, cy, r, a1, a2, n)         → circular arc
  _e(cx, cy, rx, ry, a1, a2, n)    → elliptical arc

  a1, a2 in degrees.
  Positive sweep  (a2 > a1) → counter-clockwise
  Negative sweep  (a2 < a1) → clockwise
"""

import math
from typing import List, Optional, Tuple

from ._base import Font, Glyph, Point

# ─── Arc helpers (called at import time to build glyph data) ──────────────────

def _a(cx: float, cy: float, r: float,
       a1: float, a2: float, n: int = 10) -> List[Point]:
    """Circular arc from a1° to a2°."""
    a1r = math.radians(a1)
    span = math.radians(a2 - a1)
    return [
        (round(cx + r * math.cos(a1r + i * span / max(n - 1, 1)), 4),
         round(cy + r * math.sin(a1r + i * span / max(n - 1, 1)), 4))
        for i in range(n)
    ]


def _e(cx: float, cy: float, rx: float, ry: float,
       a1: float, a2: float, n: int = 10) -> List[Point]:
    """Elliptical arc from a1° to a2°."""
    a1r = math.radians(a1)
    span = math.radians(a2 - a1)
    return [
        (round(cx + rx * math.cos(a1r + i * span / max(n - 1, 1)), 4),
         round(cy + ry * math.sin(a1r + i * span / max(n - 1, 1)), 4))
        for i in range(n)
    ]


