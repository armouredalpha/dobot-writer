class RoundedFont(Font):
    """
    Rounded font — smooth, curvy letterforms.

    Circular letters (O, C, D, G, B, P, R, Q, U, J) are drawn using
    mathematically computed arcs for genuine curve quality.  All other
    letters have extra interpolated waypoints that smooth out sharp corners.

    This font produces the most aesthetically pleasing output but requires
    more robot movements than ``ClassicFont``.
    """

    name        = "rounded"
    description = "Smooth arc-based letterforms — beautiful cursive-adjacent output"

    def get_glyph(self, char: str) -> Optional[Glyph]:
        key = char.upper() if len(char) == 1 else char
        return _ROUNDED_GLYPHS.get(key)

    def supported_chars(self) -> frozenset:
        return frozenset(_ROUNDED_GLYPHS.keys())
