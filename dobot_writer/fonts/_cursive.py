Polyline = List[Tuple[float, float]]
GlyphData = List[Polyline]


def _arc(cx, cy, rx, ry, a0, a1, n=10):
    pts = []
    for i in range(n + 1):
        t = math.radians(a0 + (a1 - a0) * i / n)
        pts.append((round(cx + rx * math.cos(t), 4),
                    round(cy + ry * math.sin(t), 4)))
    return pts


